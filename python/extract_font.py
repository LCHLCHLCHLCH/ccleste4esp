# Convert to 4-bit rows (each char = 6 rows x 4 cols, upper 4 bits per row)
chars = []
for c in range(95):
    rows = []
    for row in range(6):
        byte = pico8_font_rom[c * 8 + row]
        # byte = (left_pixel << 4) | (right_pixel & 0x0F)
        # We need 4 bits for the 4 columns: left_nibble is leftmost 4 pixels
        rows.append(byte >> 4)
    chars.append(rows)

# Print 'A'
print("'A' (idx 33):")
for r in chars[33]:
    s = "".join("#" if (r >> (3-b)) & 1 else "." for b in range(4))
    print(f"  0x{r:02X} = {s}")

# Generate header
out = "// PICO-8 standard 4x6 font (from PICO-8 ROM 0x5600)\n"
out += "// 95 chars ASCII 32-126, each 6 rows of 4 bits\n"
out += "static const uint8_t p8_font[95][6] = {\n"
for i, ch in enumerate(chars):
    c = chr(i+32) if 32 <= i+32 <= 126 else '?'
    esc = '\\\\' if c == '\\' else c
    out += f"    {{0x{ch[0]:02X},0x{ch[1]:02X},0x{ch[2]:02X},0x{ch[3]:02X},0x{ch[4]:02X},0x{ch[5]:02X}}}, // '{esc}'\n"
out += "};\n"

with open("main/font_data.h", "w") as f:
    f.write("#ifndef FONT_DATA_H\n#define FONT_DATA_H\n#include <stdint.h>\n")
    f.write(out)
    f.write("#endif\n")
print(f"Written font_data.h ({len(chars)} chars)")
