def get_std_rows(idx):
    return [p8rom[idx*8+r] >> 4 for r in range(6)]

CELL = 8
COLS = 16
ROWS = 6
img_w = COLS * CELL
img_h = ROWS * CELL

# 同时读取用户当前字体 (如果有 font.bmp)
import os
has_user = os.path.exists("font.bmp")
if has_user:
    user_img = Image.open("font.bmp").convert("RGB")
    user_pix = user_img.load()

img = Image.new("RGB", (img_w, img_h), (255, 255, 255))
pixels = img.load()

for i in range(95):
    col = i % COLS
    row = i // COLS
    cx = col * CELL
    cy = row * CELL
    std_rows = get_std_rows(i)
    
    for y in range(6):
        for x in range(4):
            # 标准字体像素 → 浅蓝色提示
            is_std = bool(std_rows[y] & (1 << (3 - x)))
            is_user = has_user and (user_pix[cx + x, cy + y][0] < 128)
            
            if is_user:
                # 用户画的 → 黑色
                pixels[cx + x,     cy + y] = (0, 0, 0)
                pixels[cx + x + 4, cy + y] = (0, 0, 0)
            elif is_std:
                # 标准字体提示 → 浅蓝
                pixels[cx + x,     cy + y] = (180, 200, 230)
                pixels[cx + x + 4, cy + y] = (180, 200, 230)
            else:
                # 空白 → 浅灰
                pixels[cx + x,     cy + y] = (220, 220, 220)
                pixels[cx + x + 4, cy + y] = (220, 220, 220)
    
    # 网格线
    for x in range(CELL):
        pixels[cx + x, cy + 7] = (180, 180, 180)
    for y in range(CELL):
        pixels[cx + 7, cy + y] = (180, 180, 180)

img.save("font_template.bmp")
